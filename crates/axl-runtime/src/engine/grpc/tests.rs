#[test]
fn grpc_unary_dispatch_round_trip() {
    // End-to-end: handler defined in .axl, server bound on :0, client
    // connects to the resolved address, sends a unary request, gets back
    // the handler's response. Exercises every part of the macro's
    // dispatch path — proto deserialization, fresh-Evaluator handler
    // invocation, Starlark→proto unpack, tonic response.
    //
    // Using ActionCache.GetActionResult since the existing `service`
    // macro produces an ActionCacheClient (Capabilities has no client
    // wrapper today).
    let result = crate::axl_eval!(
        r#"
load("@bazel//grpc.axl", "grpc")
load("@bazel//proto/remote/execution/v2.axl", "v2")

def on_get_action_result(req, rpc):
    # Echo back a size_bytes value derived from the request so we can
    # verify both directions of marshalling through the macro's dispatch.
    # `exit_code` is i32; we pack the request's `size_bytes` into it so
    # the round-trip is observable end-to-end.
    return v2.ActionResult(
        exit_code = req.action_digest.size_bytes,
    )

def on_update_action_result(req, rpc):
    return v2.ActionResult()

srv = grpc.Server(endpoint = "tcp://127.0.0.1:0")
srv.add(v2.ActionCacheService(
    on_get_action_result = on_get_action_result,
    on_update_action_result = on_update_action_result,
))
h = srv.serve()

# tonic's client endpoint parser expects http://; the server handle
# returns tcp://host:port. Rewrite for the client side.
addr = "http://" + h.endpoint[len("tcp://"):]
client = v2.ActionCacheClient(uri = addr, headers = {}, timeout = 5000)
client.connect(None)
resp = client.GetActionResult(v2.GetActionResultRequest(
    action_digest = v2.Digest(hash = "deadbeef", size_bytes = 1337),
))

h.drain_and_quit(timeout = 5000)

resp.exit_code
"#
    )
    .unwrap();
    assert_eq!(result, "1337");
}

#[test]
fn grpc_capabilities_round_trip() {
    // The driving aspect-delivery use case: a stub server that
    // advertises exec_enabled = true so Bazel's capability check passes.
    // Verifies CapabilitiesService dispatch + the newly-added
    // CapabilitiesClient (which the existing example design needs).
    let result = crate::axl_eval!(
        r#"
load("@bazel//grpc.axl", "grpc")
load("@bazel//proto/remote/execution/v2.axl", "v2")
load("@bazel//proto/semver.axl", "semver")

def _get_capabilities(req, rpc):
    return v2.ServerCapabilities(
        execution_capabilities = v2.ExecutionCapabilities(
            exec_enabled = True,
            # DigestFunction.Value enum: 1 = SHA256. Using the raw int
            # because the proto generates a nested-mod enum and the
            # nesting depth doesn't match a simple `Enum.MEMBER` access
            # path through the starbuf-derived constructor.
            digest_function = 1,
        ),
        low_api_version  = semver.SemVer(major = 2, minor = 0),
        high_api_version = semver.SemVer(major = 2, minor = 3),
    )

srv = grpc.Server(endpoint = "tcp://127.0.0.1:0")
srv.add(v2.CapabilitiesService(on_get_capabilities = _get_capabilities))
h = srv.serve()

addr = "http://" + h.endpoint[len("tcp://"):]
client = v2.CapabilitiesClient(uri = addr, headers = {}, timeout = 5000)
client.connect(None)
resp = client.GetCapabilities(v2.GetCapabilitiesRequest())

h.drain_and_quit(timeout = 5000)

[resp.execution_capabilities.exec_enabled,
 resp.execution_capabilities.digest_function,
 resp.low_api_version.major,
 resp.high_api_version.minor]
"#
    )
    .unwrap();
    // exec_enabled=True; digest_function read back as the enum's string
    // name ("sha256"); low.major=2; high.minor=3.
    assert_eq!(result, "[True, \"sha256\", 2, 3]");
}

#[test]
fn grpc_execute_round_trip() {
    // Execute is server-streaming with response type
    // `google.longrunning.Operation`. Verifies the longrunning shim is
    // reachable and that Operation flows through the macro's
    // server-streaming dispatch unchanged.
    let result = crate::axl_eval!(
        r#"
load("@bazel//grpc.axl", "grpc")
load("@bazel//proto/remote/execution/v2.axl", "v2")
load("@bazel//proto/longrunning.axl", "longrunning")

def _execute(req, stream, rpc):
    # Two-message lifecycle: queued (done=False) then completed
    # (done=True). Both reference the request's action digest hash so
    # the test verifies request marshalling end-to-end too.
    op_name = "operations/" + req.action_digest.hash
    stream.push(longrunning.Operation(name = op_name, done = False))
    stream.push(longrunning.Operation(name = op_name, done = True))
    stream.complete()

def _wait_execution(req, stream, rpc):
    rpc.abort(grpc.Code.UNIMPLEMENTED, "no wait")
    return

srv = grpc.Server(endpoint = "tcp://127.0.0.1:0")
srv.add(v2.ExecutionService(
    on_execute = _execute,
    on_wait_execution = _wait_execution,
))
h = srv.serve()

addr = "http://" + h.endpoint[len("tcp://"):]
client = v2.ExecutionClient(uri = addr, headers = {}, timeout = 5000)
client.connect(None)
stream = client.Execute(v2.ExecuteRequest(
    action_digest = v2.Digest(hash = "feedface", size_bytes = 8),
))

ops = []
for op in stream:
    ops.append((op.name, op.done))

h.drain_and_quit(timeout = 5000)
ops
"#
    )
    .unwrap();
    assert_eq!(
        result,
        "[(\"operations/feedface\", False), (\"operations/feedface\", True)]"
    );
}

#[test]
fn grpc_bytestream_service_constructible() {
    // Client-streaming dispatch surface: ByteStreamService takes a Write
    // handler whose signature is `(req_stream, rpc) -> resp`. We verify
    // construction + .add() through here; a true end-to-end Write test
    // would need a tonic client wrapper that supports passing an iterable
    // of requests, which the existing `service` macro doesn't emit (only
    // ActionCache/Capabilities/Execution/CAS have client wrappers today).
    // The Write helper + per-RPC `__WriteReqStream` are compiled-and-linked
    // by the workspace build above; this confirms the .axl-visible
    // surface works.
    let result = crate::axl_eval!(
        r#"
load("@bazel//grpc.axl", "grpc")
load("@bazel//proto/bytestream.axl", "bytestream")

def on_read(req, stream, rpc):
    rpc.abort(grpc.Code.UNIMPLEMENTED, "no read")
    return

def on_write(req_stream, rpc):
    # Sum committed_size across the incoming chunks. Exercises the iter
    # impl of the macro-emitted __WriteReqStream wrapper.
    total = 0
    for req in req_stream:
        total += len(req.data) if req.data else 0
    return bytestream.WriteResponse(committed_size = total)

def on_query(req, rpc):
    return bytestream.QueryWriteStatusResponse()

srv = grpc.Server(endpoint = "tcp://127.0.0.1:0")
srv.add(bytestream.ByteStreamService(
    on_read = on_read,
    on_write = on_write,
    on_query_write_status = on_query,
))
h = srv.serve()
h.drain_and_quit(timeout = 500)
h.endpoint.startswith("tcp://127.0.0.1:")
"#
    )
    .unwrap();
    assert_eq!(result, "True");
}

